//Objetos:
//Son contenedores de datos que pueden contener mucha información pero de una sola cosa.
//
//Propiedades:
//Es una asociación entre una clave y un valor, siempre y cuando el valor no sea una función.
//
//Métodos:
//Es una asociación entre una clave y un valor que es una función.
//
//Bucle for…in:
//Tipo de bucle usado en Objetos. Un equivalente sería el bucle for en un arreglo(array).
//
//Notación de puntos vs notación de corchetes:
//Notación de puntos: Forma de hacer el llamado a lo que se cuentre en la clave y valor del objeto deseado
//usando un punto entre la clave y el valor:
//ej: Key.value;
//
//Notación de corchetes: Forma de hacer el llamado a lo que se cuentre en la clave y valor del objeto deseado
//escribiendo la clave y luego colocando entre corchetes y comillas el valor:
//ej: key['value'];